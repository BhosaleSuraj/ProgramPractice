package flowcontrol;
// switch statement is the multiple choice decision making statement 

public class SwitshStatement 
{
	public static void main(String [] args)
	{
		int num=20;
		
		switch(num)
		{
		case 1:
			
			System.out.print(10);
			break;
			
		case 20 :
			System.out.println(20);
			
			
			
	     default:
			System.out.println("number is not matched");
			break;
			
		}
	}

}
