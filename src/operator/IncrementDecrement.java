package operator;    //Increment operator pre/post 

public class IncrementDecrement {
	public static void main(String[] args)
	{
		int a=10;
		System.out.println(a--);
		System.out.println(--a);
		System.out.println(a++);
		System.out.println(++a);
		
		
		int b=20;
		b+=5;                      //  assignment operator (+=)
		System.out.println(b);
		
	}

}
